from typing import Any, List, Dict, Tuple
from copy import copy


def calculate_recall_at_k(
    student_results: Any,
    ground_truth: Any,
) -> Tuple[List[float], List[int]]:
    """Calculates Recall@K metrics with a scaled context window.

    Evaluates the retrieval performance by measuring the overlap between
    retrieved chunks and the ground truth sources, applying a 1.5x scaling
    factor to the provided context limit.

    Args:
        student_results (Any): The search results generated by the system.
        ground_truth (Any): The reference dataset containing expected sources.

    Returns:
        list[float]: A list of average recall scores corresponding to
        each value in k_values.
    """
    k_values = [1, 3, 5, 10]
    max_context: float = 2000 * 1.5
    gt_map: Dict[str, Any] = {q.question_id: getattr(q, 'sources', [])
                              for q in ground_truth.rag_questions}
    recalls_per_k: Dict[int, List[float]] = {k: [] for k in k_values}
    for student_res in student_results.search_results:
        q_id = student_res.question_id
        if q_id not in gt_map:
            continue
        real_sources = gt_map[q_id]
        retrieved = student_res.retrieved_sources
        relevant_files = {s.file_path for s in real_sources}
        for k_val in k_values:
            found_for_this_k = 0
            top_k_candidates = retrieved[:k_val]
            current_context_len: float = 0.0
            valid_top_k = []
            for src in top_k_candidates:
                if current_context_len >= max_context:
                    break
                src_len: float = float(abs(
                    src.last_character_index - src.first_character_index
                    ))
                if src.file_path in relevant_files:
                    if current_context_len + src_len <= max_context:
                        valid_top_k.append(src)
                        current_context_len += src_len
                    else:
                        espacio_restante = max_context - current_context_len
                        src_truncado = copy(src)
                        src_truncado.last_character_index = int(
                            src.first_character_index + espacio_restante
                            )
                        valid_top_k.append(src_truncado)
                        current_context_len = max_context
                        break
            if current_context_len < max_context:
                for src in top_k_candidates:
                    if src in valid_top_k:
                        continue
                    if current_context_len >= max_context:
                        break
                    src_len = float(abs(
                        src.last_character_index - src.first_character_index
                        ))
                    espacio_restante = max_context - current_context_len
                    if src_len <= espacio_restante:
                        valid_top_k.append(src)
                        current_context_len += src_len
                    else:
                        src_truncado = copy(src)
                        src_truncado.last_character_index = int(
                            src.first_character_index + espacio_restante
                            )
                        valid_top_k.append(src_truncado)
                        current_context_len = max_context
                        break
            if not real_sources:
                recalls_per_k[k_val].append(0.0)
                continue
            for real_src in real_sources:
                longitud_real = abs(
                    real_src.last_character_index -
                    real_src.first_character_index
                    )
                if longitud_real <= 0:
                    continue
                interseccion_total = 0
                for ret_src in valid_top_k:
                    if ret_src.file_path != real_src.file_path:
                        continue
                    inicio_comun = max(
                        ret_src.first_character_index,
                        real_src.first_character_index
                        )
                    fin_comun = min(
                        ret_src.last_character_index,
                        real_src.last_character_index
                        )
                    interseccion_total += max(0, fin_comun - inicio_comun)
                if (interseccion_total / longitud_real) >= 0.05:
                    found_for_this_k += 1
            puntuacion = found_for_this_k / len(real_sources)
            recalls_per_k[k_val].append(puntuacion)
    final_scores = [
        sum(recalls_per_k[k]) / len(recalls_per_k[k])
        if recalls_per_k[k] else 0.0
        for k in k_values
    ]
    return (final_scores, k_values)
